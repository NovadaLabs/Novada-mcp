#!/usr/bin/env node
/**
 * scripts/check-hosted-drift.mjs
 *
 * DRIFT GUARD for the hosted (mcp.novada.com) tool surface.
 *
 * hosted-server/vercel/api/mcp.ts hand-curates a 15-tool `TOOLS` array that the
 * source code itself comments is a "standalone curation, NOT derived from
 * core's TOOLS" (see the comment above `const TOOLS = [` there). Nothing today
 * fails if a future edit to that array silently adds, removes, or renames a
 * tool on the MAIN entrance for all Novada MCP users. This script closes that
 * hole against a single committed manifest, config/surfaces.json (the seed of
 * the future Phase-3 config engine — see that file's $comment).
 *
 * Three gates, all must pass (the mutation gate runs FIRST and short-circuits
 * the other two — see below):
 *   (mutation gate) mcp.ts is scanned, AFTER the `TOOLS` literal's closing
 *       `].map((t) => ({` anchor, for any dynamic mutation of TOOLS
 *       (`.push`/`.splice`/`.concat`/`.unshift`, `...TOOLS` spreads,
 *       `Object.assign(TOOLS, ...)`, length-based truncate/append). A
 *       red-team demo (2026-07-18) proved this is a real bypass: appending
 *       `TOOLS.push({ name: "novada_admin_backdoor" })` AFTER the literal
 *       changes what the live endpoint serves while staying invisible to the
 *       text slice (a)/(b) below parse — so (b) returned a false PASS. If
 *       this gate finds a hit, the script exits 1 immediately: it refuses to
 *       let (a)/(b) report "no drift" against a name set it cannot vouch for.
 *   (a) every tool name in surfaces.json's "hosted-15" surface is a member of
 *       npm-package's REGISTERED_TOOL_NAMES (npm-package/src/tools/registry.ts)
 *       — no ghost/renamed tool reaches the hosted endpoint.
 *   (b) the LIVE hosted `TOOLS` array in mcp.ts, read directly off disk right
 *       now, is an exact SET match with surfaces.json's "hosted-15" list — so
 *       any future silent add/remove in mcp.ts fails this check until
 *       surfaces.json is updated in the same reviewed diff.
 *
 * ── Loading method: plain TEXT PARSING of both TypeScript source files, NOT
 *    import/execution of either. Why this is the stable choice: ──────────────
 *
 *   - hosted-server/vercel/api/mcp.ts constructs a real MCP `Server`, runs
 *     Sentry.init(), imports @vercel/kv / @vercel/functions, strips
 *     process.env consumption creds as a MODULE-LOAD side effect, and pulls
 *     from hosted-server/vercel/vendor/ (generated by deploy-hosted.sh, never
 *     hand-edited — see repo CLAUDE.md). None of that is safe, meaningful, or
 *     even possible to `import` from a plain Node script without a
 *     Vercel-shaped environment (KV creds, Sentry DSN, etc.) — and mcp.ts is
 *     TypeScript, so plain `node` cannot execute it directly regardless.
 *   - npm-package/src/tools/registry.ts IS side-effect-free by its own
 *     docstring, but importing it as TS would need either a build step
 *     (npm-package/build/tools/registry.js — a compiled artifact that can go
 *     stale relative to src/ between builds) or a TS loader (ts-node/tsx) this
 *     repo does not wire up for one-off scripts. Depending on build freshness
 *     would make this "CI-runnable node script" secretly require `npm run
 *     build` first — a hidden precondition that itself could drift.
 *
 *   Both files are instead read as plain text; the `TOOLS` / `TOOL_REGISTRY`
 *   array literals are sliced out with anchor strings unique to each file
 *   (verified against the current source before writing this script), then
 *   every `name: "novada_..."` entry inside that slice is extracted with a
 *   regex. This makes the check dependency-free (no build step, no ts-node,
 *   no Vercel env, no side effects from either real module) — it can only go
 *   stale if the anchor strings themselves are renamed, which is a rare,
 *   deliberate, easily greppable edit (unlike the tool-array CONTENTS this
 *   script exists to protect). Each extraction is asserted non-empty so a
 *   broken anchor fails loudly instead of silently passing a vacuous check.
 *
 * ── THREAT MODEL — read this before trusting (or extending) this script ──────────────────────
 *
 * A round-2 red-team pass (2026-07-19), run live against this script and mcp.ts, proved that no
 * amount of additional regex-pattern-matching in `detectDynamicToolsMutation()` can make this
 * check complete against a determined committer — text-pattern matching over a Turing-complete
 * language is fundamentally an incomplete defense, and adding more patterns only teaches the
 * next bypass what shape to avoid. Rather than expanding the mutation-gate regex further (each
 * round finds another arbitrarily-obfuscatable shape it doesn't cover), this comment states the
 * honest, bounded threat model this script actually defends, so nobody mistakes "this script
 * passes" for "this diff was safe" — those are two different claims.
 *
 * CATCHES (this script's real job):
 *   - Accidental drift: mcp.ts's hand-curated TOOLS literal and config/surfaces.json's
 *     "hosted-15" list silently falling out of sync because ONE of the two was edited and the
 *     other forgotten — gate (a)/(b) above, the manifest-compare gate.
 *   - Honest edits to the TOOLS literal itself: any name added, removed, or renamed directly in
 *     the `const TOOLS = [ ... ]` array — the exact case a reviewer expects to see as a normal,
 *     readable diff line.
 *   - Common ACCIDENTAL mutation patterns after the literal (`.push`/`.splice`/`.concat`/
 *     `.unshift`, `...TOOLS` spreads, `Object.assign(TOOLS, ...)`, length-based
 *     truncate/append) — the mutation gate is a best-effort tripwire for these specific,
 *     recognizable shapes, not a proof of absence of all dynamic mutation.
 *
 * DOES NOT DEFEND against a committer DELIBERATELY obfuscating a tool injection inside an
 * otherwise-reviewed PR. Concretely, none of the following are caught by this script, and no
 * regex-based version of this script ever could catch all of them:
 *   - A computed tool name inside the literal itself, e.g. `name: ("novada_" + "x")` or
 *     `name: SOME_CONST` — the regex `name:\s*"(novada_[a-zA-Z0-9_]+)"` only matches a literal
 *     double-quoted string; anything computed at runtime is invisible to static text parsing by
 *     construction, not by an oversight this script could patch away.
 *   - Aliased or reflected mutation of the SAME array reference under a different local name,
 *     e.g. `const S = TOOLS; S.push({...})`, `Array.prototype.push.call(TOOLS, {...})`,
 *     `Object.defineProperty(TOOLS, TOOLS.length, {...})`, or `TOOLS[0].name = "..."` — the
 *     mutation-gate patterns match the literal identifier `TOOLS` followed by a specific method
 *     name; a determined author can always introduce one more level of indirection the pattern
 *     list doesn't happen to include.
 *   - Any mutation performed from a DIFFERENT file that imports and mutates the same `TOOLS`
 *     reference (or the served list) at module-load time — this script only ever reads
 *     hosted-server/vercel/api/mcp.ts's own text.
 *   That class of attack — a reviewed PR whose diff LOOKS clean but computes/aliases its way
 *   around a textual gate — is CODE REVIEW's responsibility, not a static script's. A human
 *   reviewer reading the actual diff (not just this script's exit code) is the control that
 *   catches deliberate obfuscation; this script's job is to make the common, honest, or
 *   careless failure modes impossible to ship by accident, and to say so honestly rather than
 *   imply a false completeness guarantee.
 *
 * The COMPLETE structural fix — closing the gap by construction instead of by pattern-matching
 * — is the Phase-3 manifest refactor: mcp.ts stops hand-curating a `TOOLS` literal at all and
 * instead BUILDS its served list FROM config/surfaces.json at runtime. With no hand-curated
 * literal left to parse or inject into, there is nothing left for a computed-name or
 * aliased-mutation trick to hide inside — the served list IS the reviewed manifest, structurally,
 * not by a script's after-the-fact comparison of the two. That refactor is tracked and
 * deliberately DEFERRED (value-first sequencing — this script is the interim mitigation until
 * it lands), not implemented here.
 *
 * Usage:  node scripts/check-hosted-drift.mjs
 * Exit codes: 0 = no drift, 1 = drift detected or manifest/parse error.
 */

import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

const SURFACES_PATH = path.join(ROOT, "config", "surfaces.json");
const REGISTRY_PATH = path.join(ROOT, "npm-package", "src", "tools", "registry.ts");
const MCP_TS_PATH = path.join(ROOT, "hosted-server", "vercel", "api", "mcp.ts");

// Shared anchor pair for the hosted TOOLS array literal in mcp.ts. Used both by
// loadHostedLiveToolNames() (extracts the names INSIDE the literal) and by
// findToolsLiteralEnd() below (locates where the literal ENDS, so the
// dynamic-mutation gate knows where to start scanning). Defined once here so
// the two can never silently drift apart from each other.
const MCP_TOOLS_START_ANCHOR = "const TOOLS = [";
const MCP_TOOLS_END_ANCHOR = "].map((t) => ({";

/**
 * Slice `source` between the first occurrence of `startAnchor` and the next
 * occurrence of `endAnchor` after it, then extract every `name: "novada_xxx"`
 * tool name inside that slice. Throws (never silently returns []) if an anchor
 * is missing or the slice yields zero names — both indicate the source file's
 * shape changed and this script's anchors need updating, not a real "0 tools"
 * result.
 */
function extractToolNames(source, startAnchor, endAnchor, label) {
  const startIdx = source.indexOf(startAnchor);
  if (startIdx === -1) {
    throw new Error(
      `[check-hosted-drift] anchor not found for ${label}: ${JSON.stringify(startAnchor)} — ` +
      `file structure changed; update this script's anchors to match`
    );
  }
  const endIdx = source.indexOf(endAnchor, startIdx + startAnchor.length);
  if (endIdx === -1) {
    throw new Error(
      `[check-hosted-drift] end anchor not found for ${label}: ${JSON.stringify(endAnchor)} — ` +
      `file structure changed; update this script's anchors to match`
    );
  }
  const slice = source.slice(startIdx, endIdx);
  const names = [...slice.matchAll(/name:\s*"(novada_[a-zA-Z0-9_]+)"/g)].map((m) => m[1]);
  if (names.length === 0) {
    throw new Error(
      `[check-hosted-drift] extracted ZERO tool names for ${label} — parsing is broken, ` +
      `refusing to pass a vacuous check`
    );
  }
  return names;
}

function loadSurfacesManifest() {
  const raw = readFileSync(SURFACES_PATH, "utf8");
  return JSON.parse(raw);
}

/** REGISTERED_TOOL_NAMES equivalent: every tool name in npm-package's canonical registry. */
function loadRegisteredToolNames() {
  const src = readFileSync(REGISTRY_PATH, "utf8");
  const names = extractToolNames(
    src,
    "export const TOOL_REGISTRY: readonly ToolMeta[] = [",
    "\n];",
    "npm-package/src/tools/registry.ts TOOL_REGISTRY"
  );
  return new Set(names);
}

/** The LIVE hosted TOOLS array names, read straight off disk (see file header for why text-parsed). */
function loadHostedLiveToolNames() {
  const src = readFileSync(MCP_TS_PATH, "utf8");
  return extractToolNames(
    src,
    MCP_TOOLS_START_ANCHOR,
    MCP_TOOLS_END_ANCHOR,
    "hosted-server/vercel/api/mcp.ts TOOLS"
  );
}

/**
 * Locate the index immediately AFTER the TOOLS array literal's closing
 * `].map((t) => ({` anchor in mcp.ts — i.e. the boundary past which
 * extractToolNames() above no longer looks. Throws (never silently returns a
 * boundary of -1 or of the file length) if either anchor is missing, so a
 * renamed/reshaped literal fails loud instead of the mutation gate below
 * silently scanning zero bytes and reporting a false "clean" result.
 */
function findToolsLiteralEnd(source) {
  const startIdx = source.indexOf(MCP_TOOLS_START_ANCHOR);
  if (startIdx === -1) {
    throw new Error(
      `[check-hosted-drift] mutation-scan: start anchor not found: ${JSON.stringify(MCP_TOOLS_START_ANCHOR)} — ` +
      `file structure changed; update this script's anchors to match`
    );
  }
  const endIdx = source.indexOf(MCP_TOOLS_END_ANCHOR, startIdx + MCP_TOOLS_START_ANCHOR.length);
  if (endIdx === -1) {
    throw new Error(
      `[check-hosted-drift] mutation-scan: end anchor not found: ${JSON.stringify(MCP_TOOLS_END_ANCHOR)} — ` +
      `file structure changed; update this script's anchors to match`
    );
  }
  return endIdx + MCP_TOOLS_END_ANCHOR.length;
}

/**
 * GAP 2 hard gate (red-team finding, 2026-07-18): extractToolNames() above only
 * ever looks INSIDE the `const TOOLS = [ ... ].map((t) => ({` slice. A red-team
 * demo proved that's exploitable: appending
 *   `TOOLS.push({ name: "novada_admin_backdoor" });`
 * anywhere AFTER that slice changes the REAL set the server responds with on
 * tools/list, but is invisible to the regex above — the static check kept
 * comparing the OLD (correct) name set against surfaces.json and returned
 * PASS (b) while the live endpoint actually served an extra, unreviewed tool.
 *
 * This function scans mcp.ts text starting right after the literal's closing
 * anchor (findToolsLiteralEnd) for any shape that dynamically changes the
 * TOOLS array (or spreads/copies it into another array that could be served
 * instead) post-declaration: .push/.splice/.concat/.unshift calls, `...TOOLS`
 * spreads, Object.assign(TOOLS, ...), or length-based truncate/append tricks.
 *
 * It is intentionally NOT trying to prove what such code does at runtime —
 * text parsing cannot answer that. It only proves the static name-comparison
 * above can no longer be trusted, and the caller (main()) must treat any hit
 * here as an unconditional hard failure: refuse to pass a check it cannot
 * vouch for, rather than silently reporting drift-free when it might not be.
 */
function detectDynamicToolsMutation(source) {
  const scanStart = findToolsLiteralEnd(source);
  const tail = source.slice(scanStart);

  const patterns = [
    { re: /\bTOOLS\.push\s*\(/g, label: "TOOLS.push(...) — appends tool(s) after the literal" },
    { re: /\bTOOLS\.splice\s*\(/g, label: "TOOLS.splice(...) — inserts/removes tool(s) after the literal" },
    { re: /\bTOOLS\.concat\s*\(/g, label: "TOOLS.concat(...) — builds a served list beyond the literal" },
    { re: /\bTOOLS\.unshift\s*\(/g, label: "TOOLS.unshift(...) — prepends tool(s) after the literal" },
    { re: /\.\.\.\s*TOOLS\b/g, label: "...TOOLS — spreads TOOLS into another array (possible bypass list)" },
    { re: /\bObject\.assign\s*\(\s*TOOLS\b/g, label: "Object.assign(TOOLS, ...) — mutates TOOLS entries/shape in place" },
    { re: /\bTOOLS\.length\s*=/g, label: "TOOLS.length = ... — truncates/extends TOOLS via length assignment" },
    { re: /\bTOOLS\[\s*TOOLS\.length\s*\]\s*=/g, label: "TOOLS[TOOLS.length] = ... — index-append after the literal" },
  ];

  const hits = [];
  for (const { re, label } of patterns) {
    let m;
    while ((m = re.exec(tail)) !== null) {
      const lineNo = source.slice(0, scanStart + m.index).split("\n").length;
      hits.push(`  hosted-server/vercel/api/mcp.ts:${lineNo}: ${label}`);
    }
  }
  return hits;
}

function setsEqual(a, b) {
  if (a.size !== b.size) return false;
  for (const x of a) if (!b.has(x)) return false;
  return true;
}

function main() {
  // GAP 2 hard gate — run FIRST, unconditionally, before trusting anything the
  // rest of this script parses out of mcp.ts. If TOOLS is mutated dynamically
  // after its literal, the (a)/(b) comparisons below are computed against a
  // name set that is provably NOT what the live endpoint serves, so there is
  // no value (and real risk of a false "OK") in running them at all.
  const mcpSrcForMutationScan = readFileSync(MCP_TS_PATH, "utf8");
  const mutationHits = detectDynamicToolsMutation(mcpSrcForMutationScan);
  if (mutationHits.length > 0) {
    console.error(
      `[check-hosted-drift] FAIL (mutation-gate): TOOLS is being mutated dynamically after its ` +
      `literal; the text-based drift check cannot verify the true served surface — extract the ` +
      `tool list to config/surfaces.json and have mcp.ts read it (Phase-3 manifest refactor).`
    );
    console.error(`  Detected mutation site(s):`);
    for (const hit of mutationHits) console.error(hit);
    console.error(
      `  -> This check refuses to PASS when it cannot trust its own parse. Remove the dynamic ` +
      `mutation (fold it into the TOOLS literal itself, reviewed like any other entry) or complete ` +
      `the Phase-3 manifest refactor so mcp.ts reads its served tool list from config/surfaces.json.`
    );
    process.exit(1);
  }
  console.log(`[check-hosted-drift] PASS (mutation-gate): no dynamic mutation of TOOLS detected after its literal.`);

  const manifest = loadSurfacesManifest();
  const hosted15 = manifest?.surfaces?.["hosted-15"];
  if (!hosted15 || !Array.isArray(hosted15.tools)) {
    console.error(
      `[check-hosted-drift] FAIL: ${SURFACES_PATH} is missing a surfaces["hosted-15"].tools array`
    );
    process.exit(1);
  }
  const manifestTools = hosted15.tools;
  const manifestSet = new Set(manifestTools);
  if (manifestSet.size !== manifestTools.length) {
    console.error(
      `[check-hosted-drift] FAIL: config/surfaces.json hosted-15.tools contains duplicate name(s)`
    );
    process.exit(1);
  }

  const registeredNames = loadRegisteredToolNames();
  const hostedLiveNames = loadHostedLiveToolNames();
  const hostedLiveSet = new Set(hostedLiveNames);
  if (hostedLiveSet.size !== hostedLiveNames.length) {
    console.error(
      `[check-hosted-drift] FAIL: hosted-server/vercel/api/mcp.ts TOOLS array contains duplicate name(s)`
    );
    process.exit(1);
  }

  let failed = false;

  // (a) surfaces.json hosted-15 ⊆ REGISTERED_TOOL_NAMES — no ghost/renamed tool.
  const ghosts = manifestTools.filter((n) => !registeredNames.has(n));
  if (ghosts.length > 0) {
    failed = true;
    console.error(
      `[check-hosted-drift] FAIL (a): config/surfaces.json hosted-15 lists tool(s) NOT in the ` +
      `${registeredNames.size}-tool registry (npm-package/src/tools/registry.ts): ${ghosts.join(", ")}`
    );
  } else {
    console.log(
      `[check-hosted-drift] PASS (a): all ${manifestTools.length} hosted-15 manifest tools ∈ ` +
      `${registeredNames.size}-tool registry`
    );
  }

  // (b) live mcp.ts TOOLS array === surfaces.json hosted-15, as a SET.
  if (!setsEqual(hostedLiveSet, manifestSet)) {
    failed = true;
    const addedInMcp = [...hostedLiveSet].filter((n) => !manifestSet.has(n));
    const removedFromMcp = [...manifestSet].filter((n) => !hostedLiveSet.has(n));
    console.error(
      `[check-hosted-drift] FAIL (b): live mcp.ts TOOLS array (${hostedLiveSet.size} tools) does ` +
      `NOT match config/surfaces.json hosted-15 (${manifestSet.size} tools).`
    );
    if (addedInMcp.length) {
      console.error(`  In mcp.ts but NOT in surfaces.json (silently added/renamed on hosted): ${addedInMcp.join(", ")}`);
    }
    if (removedFromMcp.length) {
      console.error(`  In surfaces.json but NOT in mcp.ts (silently removed from hosted): ${removedFromMcp.join(", ")}`);
    }
    console.error(`  -> Update config/surfaces.json's "hosted-15" list in the SAME reviewed diff as the mcp.ts change.`);
  } else {
    console.log(
      `[check-hosted-drift] PASS (b): live mcp.ts TOOLS array (${hostedLiveSet.size} tools) matches ` +
      `config/surfaces.json hosted-15 exactly`
    );
  }

  if (failed) {
    process.exit(1);
  }
  console.log("[check-hosted-drift] OK — hosted surface is not drifting.");
}

main();
