import type { z } from "zod";
import {
  createPlatformScraperTool,
  type PlatformScraperConfig,
  type PlatformOperationConfig,
} from "./platform_scraper.js";

// ─── X (Twitter) operation → catalog scraper_id map (single source of truth) ─
// Tools-v2: novada_scrape_x, built on the config-driven platform-scraper
// factory (src/tools/platform_scraper.ts — see scrape_amazon.ts for the
// proof-of-pattern). Friendly, human-readable operation names for
// novada_scrape_x, each mapped deterministically to the exact `slug`
// (== scraper_id) in src/data/scraper_catalog.ts's x.com block.
//
// x.com is the CANONICAL catalog domain (name: "X (Twitter)") — scrape.ts's own
// resolvePlatform() aliases the common agent-typed "twitter.com" to "x.com" for
// novada_scrape, and this tool is pinned directly to platform="x.com" to match.
//
// All 3 x.com catalog operations are status:"ok" as of the 2026-07-13 live
// verification pass — none are excluded here. If a future catalog refresh marks
// any of these backend_broken, tests/tools/platform-scraper-catalog.test.ts will
// fail CI until it is removed from this map.
export const X_OPERATIONS = Object.freeze({
  post_by_url: "twitter_post_posturl",
  profile_by_username: "twitter_profile_username",
  profile_by_url: "twitter_profile_profileurl",
} as const);

export type XOperation = keyof typeof X_OPERATIONS;

/** Per-operation `params` doc — rendered as `- <name>: <doc>` in the enum description. */
const X_OPERATION_PARAMS_DOC: Record<XOperation, string> = {
  post_by_url: "params.post_url (e.g. \"https://x.com/user/status/123...\"); optional params.file_name",
  profile_by_username: "params.user_name (e.g. \"BillGates\"); optional params.file_name",
  profile_by_url: "params.profile_url (e.g. \"https://x.com/BillGates\"); optional params.file_name",
};

const X_OPERATION_CONFIGS: Record<XOperation, PlatformOperationConfig> = Object.fromEntries(
  (Object.keys(X_OPERATIONS) as XOperation[]).map((name) => [
    name,
    { scraperId: X_OPERATIONS[name], paramsDoc: X_OPERATION_PARAMS_DOC[name] },
  ]),
) as Record<XOperation, PlatformOperationConfig>;

/** X's declarative platform-scraper config — the factory's sole input for this tool. */
export const X_SCRAPER_CONFIG: PlatformScraperConfig<XOperation> = {
  platform: "x.com",
  platformLabel: "X (Twitter)",
  toolName: "novada_scrape_x",
  category: "Scraping & Verification",
  registryDescription:
    "Extract structured X (Twitter) data (post details, profile lookup by username or URL) via a closed, typed operation enum — 3 verified-working operations; same engine and output formats as novada_scrape, pinned to platform=x.com",
  operations: X_OPERATION_CONFIGS,
  paramsFieldDoc:
    "Operation-specific parameters for the selected `operation`. E.g. { post_url: \"https://x.com/user/status/123\" } for " +
    "post_by_url, { user_name: \"BillGates\" } for profile_by_username, { profile_url: \"https://x.com/BillGates\" } for profile_by_url.",
  description: {
    core:
      "Extract structured X (Twitter) data — a single post and profile lookups by username or URL — through an X-only tool with a closed, typed `operation` enum. Same underlying engine as novada_scrape, pinned to platform=\"x.com\".",
    useWhen: [
      "get the text/engagement stats for this X post URL",
      "get this X profile's info by username",
      "get this X profile's info by profile URL",
    ],
    notFor: [
      { when: "A single X/Twitter URL you just want read as plain text", useInstead: "novada_extract" },
      { when: "A general web search not scoped to X", useInstead: "novada_search" },
      { when: "A different platform's structured data (YouTube, Instagram, etc.)", useInstead: "novada_scrape with that platform's domain, or its own novada_scrape_<platform> tool" },
    ],
    returns:
      "Structured post/profile records (post text, like/reply/repost counts, profile bio, follower count, etc.) in the chosen format (markdown/json/csv/excel/html/toon) — same rendering as novada_scrape.",
    operationsNote:
      "3 verified-working X (Twitter) operations: a single post by URL, and profile lookup by username or by profile URL (see the `operation` param's description for the exact `params` keys each needs). Every x.com catalog operation is currently status:\"ok\" — none are excluded for being backend_broken.",
  },
};

/** The materialized X platform-scraper tool (definition + registry entry + handler). */
export const X_SCRAPER_TOOL = createPlatformScraperTool(X_SCRAPER_CONFIG);

export const ScrapeXParamsSchema = X_SCRAPER_TOOL.ParamsSchema;

export type ScrapeXParams = z.infer<typeof ScrapeXParamsSchema>;

export function validateScrapeXParams(args: Record<string, unknown> | undefined): ScrapeXParams {
  return X_SCRAPER_TOOL.validateParams(args);
}

/**
 * novada_scrape_x — a thin, X (Twitter)-only adapter over the shared scrape engine
 * (novadaScrape in ./scrape.js), generated by the platform-scraper factory. Resolves
 * the friendly `operation` name to its exact catalog scraper_id, pins the platform to
 * "x.com", and delegates everything else — the HTTP call, polling, output rendering,
 * and error classification — to novadaScrape. No HTTP/FormData logic is duplicated here.
 */
export async function novadaScrapeX(params: ScrapeXParams, apiKey: string): Promise<string> {
  return X_SCRAPER_TOOL.handler(params, apiKey);
}
